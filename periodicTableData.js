// Helper function to get all elements in a flat array
export const getAllElements = () => {
  const allElements = [];
  
  // Add main periods
  periodicTableData.forEach(period => {
    period.forEach(element => allElements.push(element));
  });
  
  // Add lanthanides
  lanthanides.forEach(element => allElements.push(element));
  
  // Add actinides
  actinides.forEach(element => allElements.push(element));
  
  // Sort by atomic number
  return allElements.sort((a, b) => a.number - b.number);
};

// Helper function to get element by atomic number
export const getElementByNumber = (atomicNumber) => {
  const allElements = getAllElements();
  return allElements.find(element => element.number === atomicNumber);
};

// Helper function to get elements by category
export const getElementsByCategory = (category) => {
  const allElements = getAllElements();
  return allElements.filter(element => element.category === category);
}; 